# 声明PHP编码
header("Content-type: text/html; charset=utf-8");

$array  = [1, 0, 90, 78, 110, 66, 176, 300]; // 测试数组
$max    = max($array);                       // 获得最大数(N)
$bucket = [];                                // 定义初始化容器(桶)

# 生成桶容器
for ($i=0;$i <= $max; $i++) {
    $bucket[$i] = ''; 
}

# 按照数的大小升序，在桶容器中进行排序
foreach($array as $k => $v){
    $bucket[$v] = $v;
}

# 获得桶容器长度
$count = $max-1;

# 从桶容器中取出排序结果(小到大)，大到小只需要将for写反即可
for ($k=0; $k <= $count; $k++) {

    # 大于0的数，就是桶里的排序结果
    if ($bucket[$k] !== '') {
        echo $bucket[$k]."<br>";
    }
}
