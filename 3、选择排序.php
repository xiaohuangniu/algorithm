 + Desc         : 在需要排序的一段数组中，选出最小的一个数与第一个位置的数交换。
                  然后在剩下的数当中再找最小的与第二个位置的数交换，
                  如此循环到倒数第二个数和最后一个数比较为止。
 +----------------------------------------------------------------------
